/**
 * add - add operation
 * @a: var a
 * @b: var b
 * Return: number result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract operation
 * @a: var a
 * @b: var b
 * Return: number result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply operation
 * @a: var a
 * @b: var b
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide operation
 * @a: var a
 * @b: var b
 * Return: dividend
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus operation
 * @a: var a
 * @b: var b
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
