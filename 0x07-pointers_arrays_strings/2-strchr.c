#include "main.h"
#include <stddef.h>

/**
 * _strchr - returns the first occurrence of a char in a string
 * @s: the strin being looped through
 * @c: the character being looked for
 * Return: pointer to the char's fiest occurence
 */

char *_strchr(char *s, char c)
{
	unsigned int i = 0;

	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (s + i);
	}
	return (NULL);
}
